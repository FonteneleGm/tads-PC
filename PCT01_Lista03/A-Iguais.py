# Escreva um programa que leia dois números inteiros a e b e escreva se eles são iguais.

a = int(input()) # A entrada é composta de duas linhas, a primeira linha contém o número inteiro a ( - 109 ≤ a ≤ 109)
b = int(input()) # e a segunda contém um inteiro b ( - 109 ≤ b ≤ 109).
if (a == b):
  print("Sim")
else: 
  print("Nao")